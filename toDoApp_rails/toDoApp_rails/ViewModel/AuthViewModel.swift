import Foundation

protocol AuthenticationProtocol {
    var formIsValid: Bool { get }
}

struct AuthViewModel: AuthenticationProtocol {
    var email: String?
    var password: String?
    
    var formIsValid: Bool {
        return email?.isEmpty == false && password?.isEmpty == false
    }
}
